        <h2 class="h1 title">为什么要做天赐系列基因检测</h2>
    </div>

    <p>命由天定，事在人为。基因是我们无法选择与改变的，我们能做的就是了解并接受自己的基因，调整生活方式，使先天与后天相辅相成，科学提升我们的生命品质与生活体验。</p>
    <p>基因筛查即了解基因信息的手段。通过采集受检者的DNA样本，运用先进的基因芯片和测序技术，检测与疾病或性格、智力等相关的基因，获知受检者在相关方面的遗传特征。</p>

    <div class="title-box">
            <h2 class="h1 title">步骤一：采集基因样本</h2>
    </div>
    <p>基因存在于每一个人体细胞中。采集基因样本的方式常见有：采血，取毛发的毛囊细胞，取口腔粘膜细胞。基因筛查国际通用的采样方式为取口腔粘膜细胞。
    采样使用经美国FDA认证的国际通用的一次性采样袋。用专用采样刷 <strong class="text-danger">刮取口腔内壁粘膜</strong> 。采样简单，无创伤、无疼痛仅需1分钟完成。</p>

    <div class="title-box">
            <h2 class="h1 title">步骤二：进行基因筛查</h2>
    </div>
    <p>样本采集后送到检测实验室（生物芯片上海国家工程研究中心），进行基因筛查。实验中心应用SNP基因芯片检测方法进行基因分型，该技术是基于多重PCR技术和专利的单碱基引物延伸技术完成高效SNP分型，<strong class="text-danger">准确率超过99.99％</strong>。</p>

    <div class="title-box">
            <h2 class="h1 title">步骤三：出具基因报告</h2>
    </div>
    <p>基因分型完毕后，为受检者出具：</p>
    <ul>
        <li><strong>基因分型结果：</strong>在筛查范围内，受检者所携带的基因信息，是受检者个人的基因密码。</li>
        <li><strong>基因评估报告：</strong>根据受检者的基因信息，评估受检者在相关方面的遗传倾向。</li>
        <li><strong>个性化指导报告：</strong>根据受检者的遗传特征，为受检者提供个性化的后天干预指导方案。</li>
    </ul>

    <div class="title-box">
             <h2 class="h1 title">步骤四：专业解读报告</h2>
    </div>
    <p>受检者收到基因报告后，由专业老师为受检者提供 <strong>一对一的报告讲解</strong>：让受检者更清晰的了解自己的遗传体质特征；不同的年龄段健康或教育的重点在哪里；具体应怎样执行，如饮食怎样调理，怎样加强保健，怎样教育宝宝等。具体内容视受检者所选筛查项而定。</p>

